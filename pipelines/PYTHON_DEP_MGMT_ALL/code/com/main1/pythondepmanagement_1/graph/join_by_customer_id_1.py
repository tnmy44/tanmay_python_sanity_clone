def join_by_customer_id_1(spark: SparkSession, in0: DataFrame, in1: DataFrame, ) -> DataFrame:
    return in0\
        .alias("in0")\
        .join(in1.alias("in1"), (col("in0.customer_id") == col("in1.first_name")), "left_anti")\
        .where(~ col("in0.first_name").like("%b%"))
